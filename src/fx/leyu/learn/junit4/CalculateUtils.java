package fx.leyu.learn.junit4;

public class CalculateUtils {
    
    public static int add(int a, int b) {
        return a + b;
    }
    
    public static int sub(int a, int b) {
        return a - b;
    }
    
    public static int mult(int a, int b) {
        return a * b;
    }
    
    public static int div(int a, int b) {
        return a / b;
    }
}
